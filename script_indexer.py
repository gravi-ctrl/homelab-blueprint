#!/usr/bin/env python3
# @DESCRIPTION: Creates a human-readable .MD file of every script and its function
# @FREQUENCY: Daily 5am (triggered by `backup-scripts-git.sh`)
import os
from pathlib import Path

# Load BACKUP_USER from .env
with open(os.path.join(os.path.dirname(__file__), ".env")) as f:
    BACKUP_USER = next(l.split('=')[1].strip().strip("'\"") for l in f if l.startswith('BACKUP_USER='))

ROOT_DIR = f"/home/{BACKUP_USER}/scripts"
OUTPUT_FILE = f"{ROOT_DIR}/SCRIPTS_INVENTORY.md"
EXTENSIONS = {".sh", ".py"}

def is_script_file(file_path):
    if file_path.suffix in EXTENSIONS:
        return True

    if file_path.suffix == "":
        try:
            with open(file_path, 'rb') as f:
                return f.read(2) == b'#!'
        except:
            return False

    return False

def get_metadata(file_path):
    desc = None
    freq = None

    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            for _ in range(20):
                line = f.readline().strip()
                if not line: continue

                upper_line = line.upper()
                if "@DESCRIPTION:" in upper_line:
                    desc = line.split(":", 1)[1].strip()
                elif "@FREQUENCY:" in upper_line:
                    freq = line.split(":", 1)[1].strip()
    except:
        pass

    return desc, freq

def generate_inventory():
    inventory = []

    for root, _, files in os.walk(ROOT_DIR):
        for file in files:
            file_path = Path(root) / file

            if not is_script_file(file_path):
                continue

            desc, freq = get_metadata(file_path)

            if desc is None or freq is None:
                continue

            rel_path = file_path.relative_to(ROOT_DIR)

            inventory.append({
                "script": str(rel_path),
                "desc": desc,
                "freq": freq
            })

    inventory.sort(key=lambda x: x['script'])

    md = [
        "# 📂 Script Inventory",
        "> 🤖 Auto-generated by `script_indexer.py`",
        "",
        "| Script File | Purpose | Frequency |",
        "| :--- | :--- | :--- |"
    ]

    for item in inventory:
        md.append(f"| `{item['script']}` | {item['desc']} | {item['freq']} |")

    with open(OUTPUT_FILE, 'w') as f:
        f.write("\n".join(md))

    print(f"Inventory generated: {OUTPUT_FILE}")

if __name__ == "__main__":
    generate_inventory()
